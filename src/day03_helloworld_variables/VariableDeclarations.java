package day03_helloworld_variables;

public class VariableDeclarations {
	public static void main(String[]args) {
		//Declare single variable
		int apples;
		//Declare multiple variables in single statement
		int bananas, oranges, mango;
		
		//Declare variable and assign in single statement
		int kiwi = 14;
		
		System.out.println("kiwi");
		
		//Declare and assign value to multiple variables
		//in single statement
		
		int watermelon = 11, grapes= 100, cherries = 12, apricots = 15; 
		System.out.println(watermelon + grapes + cherries - apricots);
		
		int potatoes;
		potatoes = 11;
		int onions;
		onions = 15;
		int celery;
		celery = 16;
		System.out.println(potatoes + onions + celery);
	}

}
